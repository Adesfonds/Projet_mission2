        <strong>vercorium</strong>.
    </p>

    <p>
        À la croisée de l’exploitation minière, de la science des matériaux et de l’analyse de données,
        VEM développe une approche nouvelle de l’extraction de ressources naturelles, fondée sur la
        <strong>recherche scientifique, la modélisation numérique et la responsabilité environnementale</strong>.
    </p>

    <p>
        L’activité de l’entreprise repose sur trois piliers complémentaires :
    </p>

    <ul>
        <li>L’extraction expérimentale du vercorium sur un site pilote situé dans le Vercors,</li>
        <li>La recherche scientifique, menée en partenariat avec l’Université Grenoble Alpes,</li>
        <li>La modélisation et l’analyse des données afin de comprendre et prédire les propriétés du minerai.</li>
    </ul>

    <p>
        Les premières études ont mis en évidence le potentiel stratégique du vercorium dans plusieurs
        domaines de pointe, notamment l’amélioration des batteries lithium-ion, la conception
        d’alliages ultralégers pour l’aéronautique et certaines applications expérimentales en
        électronique et semi-conducteurs.
    </p>

    <p>
        Implantée sur plusieurs sites (Valence, Vercors et Grenoble), VEM s’appuie sur une équipe
        pluridisciplinaire de scientifiques, d’ingénieurs et de techniciens. L’entreprise ambitionne de
        construire un modèle d’exploitation durable, appuyé sur des outils numériques performants et
        une gestion rigoureuse des données scientifiques et environnementales.
